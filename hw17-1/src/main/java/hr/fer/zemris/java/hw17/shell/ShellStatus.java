package hr.fer.zemris.java.hw17.shell;

/**
 * Enumeration for possible shell states.
 * 
 * @author Robert Holovka
 * @version 1.0
 */
public enum ShellStatus {
	/**
	 * Signals to the shell that it should continue.
	 */
	CONTINUE,
	/**
	 * Signals to the shell that it should terminate.
	 */
	TERMINATE;
}
